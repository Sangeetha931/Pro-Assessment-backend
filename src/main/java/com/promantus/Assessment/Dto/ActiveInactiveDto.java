package com.promantus.Assessment.Dto;

public class ActiveInactiveDto {

	private String team;
	private String isActiveCount;
	private String isInActiveCount;
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getIsActiveCount() {
		return isActiveCount;
	}
	public void setIsActiveCount(String isActiveCount) {
		this.isActiveCount = isActiveCount;
	}
	public String getIsInActiveCount() {
		return isInActiveCount;
	}
	public void setIsInActiveCount(String isInActiveCount) {
		this.isInActiveCount = isInActiveCount;
	}
	
	
}
